    public function data()
    {
        $member = Member::all();
        return datatables($member)
            ->addIndexColumn()
            ->addColumn('action', function ($member) {
                return '<button onclick="editForm(`' . route('member.update', $member->id_member) . '`)" class="btn btn-xs btn-info">Edit</button>
                <button onclick="deleteData(`' . route('member.destroy', $member->id_member) . '`)" class="btn btn-xs btn-danger">Delete</button>';
            })
            ->rawColumns(['action'])
            ->make(true);
    }

